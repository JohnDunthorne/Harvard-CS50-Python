# Compare some things

x = 3
y = 4

if x > y:
    print("x bigger than y")
elif y > x:
    print("x smaller than y")
else:
    print("x is equal to y")

# Check to see if x is equal to y, or not

if x > y or x < y:
    print("X is not equal to Y")
else:
    print("X is equal to Y")

